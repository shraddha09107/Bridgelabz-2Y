import static java.lang.Math.*;

public class StaticImportDemo {
    public static void main(String[] args) {
        System.out.println("Square root of 25: " + sqrt(25));
        System.out.println("Power(2,3): " + pow(2, 3));
        System.out.println("Max(15,10): " + max(15, 10));
        System.out.println("Min(15,10): " + min(15, 10));
        System.out.println("Absolute(-9.5): " + abs(-9.5));
    }
}
